    private void AddWikiGuideResults()
    {
        foreach (var entry in WikiGuideEntries)
        {
            var result = new GuideSearchResult
            {
                Category = entry.Category,
                Title = entry.Title,
                Detail = entry.Detail
            };

            if (entry.Aliases.Length > 0)
            {
                var aliasSection = new GuideSearchSection { Title = "别名" };
                aliasSection.Lines.Add($"别名：{string.Join("、", entry.Aliases)}");
                result.Sections.Add(aliasSection);
            }

            foreach (var sourceSection in entry.Sections)
            {
                var section = new GuideSearchSection { Title = sourceSection.Title };
                section.Lines.AddRange(sourceSection.Lines);
                result.Sections.Add(section);
            }

            if (entry.Links.Length > 0)
            {
                var related = new GuideSearchSection { Title = "相关搜索" };
                related.Actions.AddRange(entry.Links.Select(link => new GuideSearchAction
                {
                    Label = link.Label,
                    Query = link.Query,
                    Detail = link.Detail
                }));
                result.Sections.Add(related);
            }

            var source = new GuideSearchSection { Title = "资料来源" };
            source.Lines.Add($"{entry.SourceTitle}：{entry.SourceUrl}");
            result.Sections.Add(source);

            _searchIndex.Add(result);
        }
    }

    private sealed record WikiGuideEntry(
        string Category,
        string Title,
        string Detail,
        string[] Aliases,
        WikiGuideSection[] Sections,
        WikiGuideLink[] Links,
        string SourceTitle,
        string SourceUrl);

    private sealed record WikiGuideSection(
        string Title,
        string[] Lines);

    private sealed record WikiGuideLink(
        string Label,
        string Query,
        string Detail);
